package curso;

public class Atribuicao {

	public static void main(String[] args) {
	
		int xa = 10;
		int xb = xa++;
		System.out.println(xa);
		System.out.println(xb);
		System.out.println(xb);
		System.out.println(xa);
		
		System.out.println("-------------");
		int xc = 10;
		int xd = ++xc;
		System.out.println(xc);
		System.out.println(xd);

	}

}
